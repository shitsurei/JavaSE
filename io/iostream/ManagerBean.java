package io.iostream;

public class ManagerBean {
	private String id;
	private String name;
	private int age;
	private boolean isLeader;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public boolean isLeader() {
		return isLeader;
	}
	public void setLeader(boolean gender) {
		this.isLeader = gender;
	}
	
}
